                <div class="div-shadow white">
                	<h3 class="bold">Directions:</h3>
                    <ol>
                        <li>In a saucepan, heat 2 tablespoons oil over medium heat. Add barley and sauté until lightly toasted, stirring frequently to prevent burning. Add stock and bring to a boil. Reduce heat to low and simmer gently, covered, for 25 minutes or until liquid is absorbed.</li>
                        <li>Remove from heat and set aside to cool. In a sauté pan heat remaining 1 tablespoon oil over medium heat. Add mushrooms and sauté until browned and soft, stirring frequently to prevent burning. Remove from heat and add to the cooled barley. Stir to combine.</li>
                        <li>Transfer barley/mushroom mixture to a large serving bowl. Add beans, green peppers, corn, tomatoes, onions, jalapeno pepper, basil, garlic, juice and pumpkin seeds. Using a large wooden spoon, gently stir to combine well. Season with salt and pepper. Stir to combine. Serve at room temperature.</li>
                    </ol>
