                <h1>Tuscan Bean Soup</h1>
            </div>
            <div class="col-sm-4">
            	<div class="div-shadow white">
                	<p class="title bold"><i class="material-icons">restaurant</i>Makes 4 quarts</p>
                    <h3 class="bold">Ingredients:</h3>
                    <ul>
                    <li><span class="bold">1/8</span> pound bacon, chopped</li>
					<li><span class="bold">1</span> Tbsp. olive oil</li>
					<li><span class="bold">1</span> cup carrots, washed, peeled, diced</li>
					<li><span class="bold">1</span> cup onions, diced</li>
					<li><span class="bold">1/2</span> cup celery, washed, diced</li>
					<li><span class="bold">1</span> white potato, peeled, 1/4-inch dice</li>
					<li><span class="bold">1 1/2</span> large red bell peppers, washed, seeded, medium dice</li>
					<li><span class="bold">6</span> cups white beans, cooked, drained</li>
					<li><span class="bold">1 1/2</span> quarts water</li>
					<li><span class="bold">1</span> cup whole tomatoes, canned, processed slightly in food processor (or blender)</li>
					<li><span class="bold">2</span> tsp. salt</li>
					<li><span class="bold">3/4</span> tsp. red pepper flakes</li>
					<li><span class="bold">3/4</span> tsp. freshly cracked black pepper</li>
					<li><span class="bold">1/2</span> tsp. rosemary, fresh, washed</li>
                    </ul>
                </div>
            </div>
            <div class="col-sm-8">
