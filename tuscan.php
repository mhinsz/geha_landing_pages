               	<div class="div-shadow dark">
                	<p>Beans can help lower blood pressure thanks to their high fiber content, potassium and magnesium. (Source: GB HealthWatch)</p>
                </div>
                <div class="div-shadow white">
                	<h3 class="bold">Directions:</h3>
                    <ol>
                        <li>In a 1-gallon stock pot, sauté bacon over medium-high heat until lightly crisp, stirring constantly. Add oil, carrots, onions, celery, potato, and peppers and sauté until halfway tender, stirring constantly to prevent burning. Add beans, water, tomatoes, salt, red pepper flakes, black pepper and rosemary.</li>
                        <li>Stir to mix and bring to a boil. Reduce heat to simmer and continue to cook for an additional 15 minutes.</li>
                        <li>Remove from heat. To serve, ladle into individual soup bowls. Serve warm.</li>
                    </ol>
                    <p><span class="bold">ETC:</span> Serve a moist cornbread and honey butter alongside this soup for a full meal experience.</p>
